// Get the image container and the arrow buttons
const imageContainer = document.querySelector('.imagee');
const leftArrow = document.getElementById('left-arrow');
const rightArrow = document.getElementById('right-arrow');

// Set the scroll speed (images width + gap)
const scrollAmount = imageContainer.querySelector('img').offsetWidth + 15; // Image width + gap

// Function to scroll left
leftArrow.addEventListener('click', function() {
  imageContainer.scrollBy({
    left: -scrollAmount, // Scroll to the left
    behavior: 'smooth'   // Smooth scroll effect
  });
});

// Function to scroll right
rightArrow.addEventListener('click', function() {
  imageContainer.scrollBy({
    left: scrollAmount,  // Scroll to the right
    behavior: 'smooth'   // Smooth scroll effect
  });
});